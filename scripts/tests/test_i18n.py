class TestValidateKeyName(unittest.TestCase):
    """Key 命名校验测试。"""

    def test_valid_keys(self):
        for key in ["Common_OK", "Settings_Title", "Member_MemberCountFmt", "Guide_Phase0_Step0_Title"]:
            with self.subTest(key=key):
                self.assertEqual(validate_key_name(key), [])

    def test_missing_underscore(self):
        issues = validate_key_name("CommonOK")
        self.assertTrue(any("命名规范" in i for i in issues))

    def test_lowercase_start(self):
        issues = validate_key_name("common_OK")
        self.assertTrue(any("命名规范" in i for i in issues))

    def test_double_underscore(self):
        issues = validate_key_name("Common__OK")
        self.assertTrue(any("连续下划线" in i for i in issues))

