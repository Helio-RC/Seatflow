def make_resx_content(entries: list[dict]) -> str:
    """构造完整的 .resx 文件内容。"""
    lines = [RESX_PREAMBLE]
    for e in entries:
        name = e["name"]
        value = xml_escape(e["value"])
        if e.get("comment"):
            comment = xml_escape(e["comment"])
            lines.append(
                f'  <data name="{name}" xml:space="preserve">\n'
                f"    <value>{value}</value>\n"
                f"    <comment>{comment}</comment>\n"
                f"  </data>"
            )
        else:
            lines.append(
                f'  <data name="{name}" xml:space="preserve">\n'
                f"    <value>{value}</value>\n"
                f"  </data>"
            )
    lines.append("</root>\n")
    return "\n".join(lines)


def make_designer_cs_content(keys: list[str], interludes: dict[str, list[str]] = None) -> str:
    """构造 Designer.cs 内容。"""
    lines = [
        "#nullable enable",
        "// <auto-generated />",
        "namespace A_Pair.Presentation.Avalonia.Lang;",
        "",
        "using System;",
        "",
        '[global::System.CodeDom.Compiler.GeneratedCode("System.Resources.Tools.StronglyTypedResourceBuilder", "17.0.0.0")]',
        "[global::System.Diagnostics.DebuggerNonUserCode]",
        "[global::System.Runtime.CompilerServices.CompilerGenerated]",
        "public class Resources",
        "{",
        "    private static global::System.Resources.ResourceManager? _resourceMan;",
        "",
        '    [global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Advanced)]',
        "    public static global::System.Resources.ResourceManager ResourceManager",
        "    {",
        "        get",
        "        {",
        "            if (ReferenceEquals(_resourceMan, null))",
        '                _resourceMan = new global::System.Resources.ResourceManager(',
        '                    "A_Pair.Presentation.Avalonia.Lang.Resources", typeof(Resources).Assembly);',
        "            return _resourceMan;",
        "        }",
        "    }",
        "",
        '    [global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Advanced)]',
        "    public static global::System.Globalization.CultureInfo Culture { get; set; } = global::System.Globalization.CultureInfo.CurrentUICulture;",
        "",
    ]
    interludes = interludes or {}
    for key in keys:
        if key in interludes:
            lines.extend(interludes[key])
        lines.append(f'    public static string {key} => ResourceManager.GetString("{key}", Culture)!;')
    lines.append("}")
    lines.append("")
    return "\n".join(lines)


